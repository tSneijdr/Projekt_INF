package gui;

public enum Mode {
	Standard("Standard"),
	TemporalX("Auslenkung in X-Richtung anhand der Reihenfolge der Beobachtung."),
	TemporalY("Auslenkung in Y-Richtung anhand der Reihenfolge der Beobachtung.");

	private final String description;

	Mode(String description) {
		this.description = description;
	}

	public String getDescription() {
		return description;
	}
}